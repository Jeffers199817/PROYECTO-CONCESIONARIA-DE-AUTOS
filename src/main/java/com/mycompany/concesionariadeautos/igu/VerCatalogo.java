    public void cargarTabla() {
        
        
        //Definir el modelo que quermos que tenga la tabal 
        
        DefaultTableModel modelTabla = new DefaultTableModel(){
            
           @Override
           public boolean isCellEditable(int row, int column){
               return false;
           } 
        };
        
        //Establecemos los nombre de las columnas 
        
        String titulos[] = {"Num","Modelo","Marca","Motor","Color","Placa","Cantidad de Puertas"};
        modelTabla.setColumnIdentifiers(titulos);
        
        //Carga de los datos desde la base de datos 
        
        List<Automovil> listaAuto = control.traerAutos();

        //Recorre la lista y mostrar cada uno de los elementos en la tabla
        
        if(listaAuto !=null){
            
            for(Automovil autos: listaAuto){ 
                Object[] objeto = { autos.getId(), autos.getModelo(),
                    autos.getMarca(), autos.getMotor(), autos.getColor(), autos.getPlaca(),autos.getCantidadPuertas()};
                    
                
                
                modelTabla.addRow(objeto);
                }
            
            tbListaAutos.setModel(modelTabla);
            }
            
            
        }

    private void ventanaAlerta(String mensaje, String tipo, String mensaj) {
        
        
  JOptionPane optionPane = new JOptionPane(mensaje);
        
        if(tipo.equals("info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        if(tipo.equals("error")){ 
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        
        JDialog dialog = optionPane.createDialog(mensaj);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        cargarTabla();
        
    }


    
        
    
